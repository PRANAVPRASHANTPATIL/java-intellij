public class Escape {
    public static void main(String[] args){
        System.out.println("hello \"Pranav\"");
        System.out.println("hello \'Pranav\'");
        System.out.println("hello \nPranav\n");
        System.out.println("hello \tPranav\t"); 
        System.out.println("hello \\Pranav\\");
        System.out.println("hello \bPranav\b");



    }
}
